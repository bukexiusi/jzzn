    public static void main(String[] args) {
        int M = 8;
        int[] array = {2, 3, 4, 5};
        int[] fibo = new int[M];
        for (int i = 0; i < M; i++) {
            if (i == 0) {
                fibo[i] = 0;
            }
        }
    }
