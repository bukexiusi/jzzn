def collect_data_to_data_format(data):
    result = {}
    for k1, v1 in data.items():
        data_source_set = result.get(k1)
        if not data_source_set:
            data_source_set = []
            result[k1] = data_source_set

        cycle_count = 0
        cycle_count_mapping = {}
        # 确定循环次数（生成对象数）
        for k2, v2 in v1.items():
            for k3, v3 in v2.items():
                for k4, v4 in v3.items():
                    cycle_count_mapping[cycle_count] = [k3, k4]
                    cycle_count = cycle_count + 1
            break

        for cycle_index in range(cycle_count):
            obj_format = {}
            for k2, v2 in v1.items():
                # v2: {0: {0: "160"}}
                current_page_index = cycle_count_mapping.get(cycle_index)[0]
                current_row_index = cycle_count_mapping.get(cycle_index)[1]
                page_data = v2.get(current_page_index)
                if not page_data:
                    break
                row_data = page_data.get(current_row_index)
                obj_format[k2] = row_data

            data_source_set.append(obj_format)

    print(result)


